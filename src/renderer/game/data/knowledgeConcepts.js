/** All concept IDs. */
export const ALL_CONCEPT_IDS = Object.keys(KNOWLEDGE_CONCEPTS);

/** Get concepts by category. */
export function getConceptsByCategory(category) {
  return Object.values(KNOWLEDGE_CONCEPTS).filter((c) => c.category === category);
}

/** Get concepts that a specific unlock event would grant. */
export function getConceptsForEvent(eventType, eventId) {
  return Object.values(KNOWLEDGE_CONCEPTS).filter((c) => {
    const by = c.unlockedBy;
    if (!by) return false;
    if (eventType === 'quest' && by.quest === eventId) return true;
    if (eventType === 'build' && by.build === eventId) return true;
    if (eventType === 'factory' && by.factory === eventId) return true;
    if (eventType === 'concept' && (by.concept === eventId || by.concept2 === eventId)) return true;
    return false;
  });
}
